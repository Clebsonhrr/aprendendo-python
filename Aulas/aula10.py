# Soma dois valores: 10 + 10 = 20
adicao = 10 + 10
print('Adição', adicao)  # Exibe: Adição 20

# Subtrai dois valores: 10 - 5 = 5
subtracao = 10 - 5
print('Subtração', subtracao)  # Exibe: Subtração 5

# Multiplica dois valores: 10 * 10 = 100
multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)  # Exibe: Multiplicação 100

# Divide dois valores: 10 / 3 = 3.333... (resultado com casas decimais - tipo float)
divisao = 10 / 3
print('Divisão', divisao)  # Exibe: Divisão 3.3333333333333335

# Faz uma divisão inteira (descarta o que sobra depois da vírgula): 10 // 3 = 3
divisao_inteira = 10 // 3
print('Divisão inteira', divisao_inteira)  # Exibe: Divisão inteira 3

# Exponenciação: 2 elevado à 10ª potência = 2 ** 10 = 1024
exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)  # Exibe: Exponenciação 1024

# Módulo: mostra o resto da divisão. 55 % 2 = 1 (pois 55 dividido por 2 dá 27 e sobra 1)
modulo = 55 % 2
print('Módulo', modulo)  # Exibe: Módulo 1

# As linhas abaixo verificam se o número à esquerda é divisível pelo da direita (resto = 0)

print(10 % 8 == 0)  # False, pois 10 dividido por 8 sobra 2
print(16 % 8 == 0)  # True, pois 16 dividido por 8 sobra 0
print(10 % 2 == 0)  # True, pois 10 dividido por 2 sobra 0
print(15 % 2 == 0)  # False, pois 15 dividido por 2 sobra 1
print(16 % 2 == 0)  # True, pois 16 dividido por 2 sobra 0
